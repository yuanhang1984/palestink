package library.bool;

/**
 * 用于可以对象传递的布尔类
 */
public class Bool {
        private boolean bool;

        public Bool(boolean bool) {
                this.bool = bool;
        }

        public boolean isBool() {
                return bool;
        }

        public void setBool(boolean bool) {
                this.bool = bool;
        }
}
